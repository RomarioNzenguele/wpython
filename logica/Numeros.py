# Operações Aritméticas

print(4 + 4)  # Soma
print(3 - 4)  # Subtração
print(4 * 2)  # Multiplicação
print(4 / 3)  # Divisão
print(4 ** 2)  # Potência
print(10 % 3)  # Módulo


# Operações com Números Float

print(10/4)  # Resultado é um número float
print(10//4)  # Resultado apenas a parte inteira


# Casting
x = float(9)  # 9.0
x = int(6.5)  # 6
x = int(float('2.5'))  # 2
print(x)
